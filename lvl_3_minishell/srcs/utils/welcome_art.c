#include "../../includes/minishell.h"

void	welcome_art(void)
{
	char	art[845 * 6];
	int		fd;

	fd = open("./srcs/utils/art.txt", O_RDONLY);
	if (fd == -1)
		perror("Failed to open welcome art");
	read(fd, art, 845 * 6);
	art[845 * 5] = '\0';
	close(fd);
	printf("%s\n", art);
}

